import 'home_page_column_copy_model.dart';
export 'home_page_column_copy_model.dart';

class HomePageColumnCopyWidget extends StatefulWidget {
  const HomePageColumnCopyWidget({super.key});

  @override
  _HomePageColumnCopyWidgetState createState() =>
      _HomePageColumnCopyWidgetState();
}

class _HomePageColumnCopyWidgetState extends State<HomePageColumnCopyWidget> {
  late HomePageColumnCopyModel _model;

  final scaffoldKey = GlobalKey<ScaffoldState>();

  @override
  void initState() {
    super.initState();
    _model = createModel(context, () => HomePageColumnCopyModel());
  }

  @override
  void dispose() {
    _model.dispose();

    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    if (isiOS) {
      SystemChrome.setSystemUIOverlayStyle(
        SystemUiOverlayStyle(
          statusBarBrightness: Theme.of(context).brightness,
          systemStatusBarContrastEnforced: true,
        ),
      );
    }

    context.watch<FFAppState>();

    return GestureDetector(
      onTap: () => _model.unfocusNode.canRequestFocus
          ? FocusScope.of(context).requestFocus(_model.unfocusNode)
          : FocusScope.of(context).unfocus(),
      child: Scaffold(
        key: scaffoldKey,
        backgroundColor: const Color(0x09002F34),
      ),
    );
  }
}
